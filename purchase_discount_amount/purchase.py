from openerp.osv import fields, osv
import openerp.addons.decimal_precision as dp


class purchase_order_line(osv.osv):

    _inherit = 'purchase.order.line'

    def _amount_line(self, cr, uid, ids, prop, arg, context=None):
        res = {}
        cur_obj=self.pool.get('res.currency')
        tax_obj = self.pool.get('account.tax')
        for line in self.browse(cr, uid, ids, context=context):
            taxes = tax_obj.compute_all(cr, uid, line.taxes_id, line.price_unit, line.product_qty, line.product_id, line.order_id.partner_id)
            cur = line.order_id.pricelist_id.currency_id
            res[line.id] = cur_obj.round(cr, uid, cur, taxes['total']) - line.discount_total
        return res

    _columns = {
        'discount_amount': fields.float('Discount On Price', digits_compute=dp.get_precision('Discount')),
        'discount_total': fields.float('Discount On Total', digits_compute=dp.get_precision('Discount')),
        'discount': fields.float('Discount (%)', digits=(16, 12)),
        'price_subtotal': fields.function(_amount_line, string='Subtotal', digits_compute= dp.get_precision('Account')),
    }

    def onchange_discount_amount(self, cr, uid, ids, discount_amount, price_unit, context=None):
        val = {'discount': 0.0}
        if price_unit:
            discount = float(discount_amount) / float(price_unit) * 100
            val['discount'] = discount
        return {'value': val}

